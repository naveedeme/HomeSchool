  function parseUrduToEnglishData(raw) {
    const lines = String(raw || "").split(/\r?\n/);
    const days = [];
    let currentDay = null;

    for (let index = 0; index < lines.length; index += 1) {
      const rawLine = lines[index];
      const line = rawLine.trim();
      if (!line || line === "---") continue;

      const dayMatch = line.match(/^#\s*✅\s*Day\s*(\d+)\s*[–-]\s*(.+)$/i);
      if (dayMatch) {
        currentDay = {
          day: Number(dayMatch[1]),
          title: dayMatch[2].trim(),
          words: [],
        };
        days.push(currentDay);
        continue;
      }

      if (!currentDay) continue;

      const urduMatch = line.match(/^\d+\.\s*(.+)$/);
      if (!urduMatch) continue;

      let english = "";
      for (let offset = index + 1; offset < lines.length; offset += 1) {
        const nextLine = lines[offset].trim();
        if (!nextLine) continue;
        if (nextLine === "---") break;
        if (/^#\s*✅\s*Day\s*/i.test(nextLine)) break;
        if (/^\d+\.\s*/.test(nextLine)) break;
        english = nextLine;
        break;
      }

      currentDay.words.push({
        ur: urduMatch[1].trim(),
        en: english.trim(),
      });
    }

    return days.map((day) => ({
      ...day,
      paragraph: day.words.map((entry) => entry.en).filter(Boolean).join(" "),
    }));
  }

  window.HomeSchoolEnglishModules.URDU_TO_ENGLISH_DATA = parseUrduToEnglishData(RAW_URDU_TO_ENGLISH);
